void KPrintChar( int px, int py, unsigned short c) { //カナ一文字表示
    DISPGRAPH kfont;
    GRAPHDATA kfont_info;
 
    c = (c&0xFF) - 0xA0;
 
    kfont_info.width = 6;
    kfont_info.height = 8;
    kfont_info.pBitmap = KanaFont[c];
    kfont.x = px;
    kfont.y = py;
    kfont.GraphData = kfont_info;
    kfont.WriteModify = IMB_WRITEMODIFY_NORMAL;
    kfont.WriteKind = IMB_WRITEKIND_OVER;
    Bdisp_WriteGraph_VRAM(&kfont);
}
