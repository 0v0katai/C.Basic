void KPrintCharSub( int px, int py, unsigned char *c, int modify ) { //カナ一文字表示	px,py  (0,-24)-(383,191)
    DISPGRAPH kfont;
    GRAPHDATA kfont_info;
    int a,b;

	b= (*c);
	a= (b<<8) + c[1];
 	if ( ( ( ExtCharKanaFX )&&( 0xFFA0 <= a )&&( a <= 0xFFEF ) ) || ( ( ExtCharGaijiFX )&&( 0xFF80 <= a )&&( a <= 0xFF9F ) ) || ( ( ExtCharAnkFX )&&( 0x20 <= b )&&( b < 0x7F ) ) ) {
		if ( ( 0x20 <= b )&&( b < 0x7F ) ) {
		    kfont_info.pBitmap = ExtAnkFontFX + (b-0x20)*8;
		} else {
			a &= 0xFF;
			a -= 0x80;
			if ( a>=0x70 ) goto err ;
		    kfont_info.pBitmap = ExtKanaFontFX + a*8;
		}
	} else {
		if ( ( 0xE741 <= a )&&( a <= 0xE77E ) ) {
			a &= 0xFF;
			a -= 0x40;
			kfont_info.pBitmap = FontE7[a];
		} else 
		if ( ( 0xFF80 <= a )&&( a <= 0xFFE2 ) ) {
			a &= 0xFF;
			a -= 0x80;
			kfont_info.pBitmap = KanaFont[a];
		} else {
		  err:
			locate (px/6+1,py/8+1);
			if ( modify == IMB_WRITEMODIFY_NORMAL ) PrintC( (unsigned char*)"@" );
			else									PrintRevC( (unsigned char*)"@" );
			return ;
		}
	}
    kfont_info.width = 6;
    kfont_info.height = 8;
    kfont.x = px;
    kfont.y = py;
    kfont.GraphData = kfont_info;
    kfont.WriteModify = modify;
    kfont.WriteKind = IMB_WRITEKIND_OVER;
    Bdisp_WriteGraph_VRAM(&kfont);
}
void KPrintChar( int px, int py, unsigned char *c) { //カナ一文字表示	px,py  (0,-24)-(383,191)
	KPrintCharSub( px, py, c, IMB_WRITEMODIFY_NORMAL);
}
void KPrintRevChar( int px, int py, unsigned char *c) { //カナ一文字表示	px,py  (0,-24)-(383,191)
	KPrintCharSub( px, py, c, IMB_WRITEMODIFY_REVERCE);
}


