/* *****************************************************************************
 *  Standard font data format
 * *****************************************************************************
 * 
 * Every character is stored in an array of 8 values.
 * Each value represents the sum of 6 active pixels in its corresponding line.
 * 
 * For example, `{0x38,0x44,0x04,0x34,0x54,0x54,0x38,0x00}` will produce:
 *      . . @ @ @ .  // 0x38 = 00111000
 *      . @ . . . @  // 0x44 = 01000100
 *      . . . . . @  // 0x04 = 00000100
 *      . . @ @ . @  // 0x34 = 00110100
 *      . @ . @ . @  // 0x54 = 01010100
 *      . @ . @ . @  // 0x54 = 01010100
 *      . . @ @ @ .  // 0x38 = 00111000
 *      . . . . . .  // 0x00 = 00000000
 * 
 * ************************************************************************** */

