/** The size of mini font character ranges from 2 to 6 pixels by 6 lines.
    The first value determines the width of the hover cursor when it is in
    the corresponding character, while the next one should always be 0.
    The remaining parts are converted to binary, where the first 6 digits
    represents the status of 6 pixels in its corresponding line.
    For instance, `{6,0,0x70,0x08,0x68,0x98,0x70,0x00}` will produce:

        . @ @ @ . .     0x70 = 01110000
        . . . . @ .     0x08 = 00001000
        . @ @ . @ .     0x68 = 01101000
        @ . . @ @ .     0x98 = 10011000
        . @ @ @ . .     0x70 = 01110000
        . . . . . .     0x00 = 00000000
    
    To comply with existing characters in Casio Basic, the character glyph
    should have one line of space on the left and at the bottom.
    
    Use `{4,0,0x0, 0x0, 0x0, 0x0, 0x0, 0x0 }` to indicate a null font data. */

