void StatusArea_SetGlyph( int mode ) {	// mode:0 file   1:edit   2:run
	TStatusGlyphDefinition	stgd ;

	stgd.dx = 27; 
	stgd.dy = 18 ;
	switch ( Angle ) { 
		case 0:	// Deg
			stgd.shortcolor_bitmap = (void*) Icon_Deg ;
			break;
		case 1:	// Rad
			stgd.shortcolor_bitmap = (void*) Icon_Rad ;
			break;
		case 2:	// Grad
			stgd.shortcolor_bitmap = (void*) Icon_Grad ;
			break;
	}
	stgd.xalignment =  1 ;
	stgd.xoffset    =  0 ;

	DefineStatusGlyph( 0, &stgd );		// 0


	stgd.dx = 45; 
	stgd.dy = 18 ;
	switch ( CB_INT ) { 
		case 0:	// DBL
			if ( MatBase )	stgd.shortcolor_bitmap = (void*) Icon_Dbl1 ;
			else			stgd.shortcolor_bitmap = (void*) Icon_Dbl0 ;
			break;
		case 1:	// INT
			if ( MatBase )	stgd.shortcolor_bitmap = (void*) Icon_Int1 ;
			else			stgd.shortcolor_bitmap = (void*) Icon_Int0 ;
			break;
		case 2:	// CPLX
			if ( MatBase )	stgd.shortcolor_bitmap = (void*) Icon_Cplx1 ;
			else			stgd.shortcolor_bitmap = (void*) Icon_Cplx0 ;
			break;
	}
	stgd.xalignment =  1 ;
	stgd.xoffset    = 29 ;

	DefineStatusGlyph( 1, &stgd );		// 1

	stgd.dx = 29; 
	stgd.dy = 18 ;
	switch ( CB_G1MorG3M ) { 
		case 1:	// G1M
			stgd.shortcolor_bitmap = (void*) Icon_G1M ;
			break;
		case 3:	// G3M
			stgd.shortcolor_bitmap = (void*) Icon_G3M ;
			break;
	}
	stgd.xalignment =  1 ;
	stgd.xoffset    = 29+47 ;

	DefineStatusGlyph( 2, &stgd );		// 2


	stgd.dx = 30; 
	stgd.dy = 18 ;
	switch ( ComplexMode ) { 
		case 0:	// Real
			stgd.shortcolor_bitmap = (void*) Icon_Real ;
			break;
		case 1:	// a+bi
			stgd.shortcolor_bitmap = (void*) Icon_a_bi ;
			break;
		case 2:	// r_theta
			stgd.shortcolor_bitmap = (void*) Icon_r_theta ;
			break;
	}
	stgd.xalignment =  1 ;
	stgd.xoffset    = 29+47+31 ;

	DefineStatusGlyph( 3, &stgd );		// 3

}

